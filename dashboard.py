            # Bid (Предложение) - лучшая цена, по которой кто-то готов купить (мы можем продать)
            # Ask (Запрос) - лучшая цена, по которой кто-то готов продать (мы можем купить)
            bid_строка = инфо_тикера.get("bid_price", "N/A") or "N/A"
